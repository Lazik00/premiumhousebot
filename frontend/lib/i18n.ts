type UnitKey = keyof typeof unitForms;

function resolvePath(tree: TranslationTree, path: string): string | undefined {
    const value = path.split('.').reduce<string | TranslationTree | undefined>((current, segment) => {
        if (!current || typeof current === 'string') return undefined;
        return current[segment];
    }, tree);

    return typeof value === 'string' ? value : undefined;
}

function interpolate(template: string, variables: Record<string, string | number> = {}): string {
    return template.replace(/\{(\w+)\}/g, (_, key) => String(variables[key] ?? ''));
}

function pluralIndexRu(count: number): number {
    const mod10 = count % 10;
    const mod100 = count % 100;

    if (mod10 === 1 && mod100 !== 11) return 0;
    if (mod10 >= 2 && mod10 <= 4 && (mod100 < 12 || mod100 > 14)) return 1;
    return 2;
}

export function translate(language: LanguageCode, key: string, variables?: Record<string, string | number>): string {
    const dictionary = translations[language] ?? translations.uz;
    const fallback = resolvePath(translations.uz, key) ?? key;
    const phrase = resolvePath(dictionary, key) ?? fallback;
    return interpolate(phrase, variables);
}

export function resolveLanguage(code: string | undefined): LanguageCode {
    if (!code) return 'uz';
    if (code.startsWith('ru')) return 'ru';
    if (code.startsWith('en')) return 'en';
    return 'uz';
}

export function formatUnitCount(language: LanguageCode, unit: UnitKey, count: number): string {
    const forms = unitForms[unit][language];
    if (language === 'uz') {
        return `${count} ${forms[0]}`;
    }
    if (language === 'ru') {
        return `${count} ${forms[pluralIndexRu(count)]}`;
    }
    return `${count} ${forms[count === 1 ? 0 : 1]}`;
}

export function formatLocalizedDate(
    value: string,
    language: LanguageCode,
    options: Intl.DateTimeFormatOptions,
): string {
    return new Date(value).toLocaleDateString(localeByLanguage[language], options);
}

export function getAmenityLabel(amenity: Amenity, language: LanguageCode): string {
    if (language === 'ru') return amenity.name_ru || amenity.name_uz;
    if (language === 'en') return amenity.name_en || amenity.name_uz;
    return amenity.name_uz;
}
